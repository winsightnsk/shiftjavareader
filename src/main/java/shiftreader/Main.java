package shiftreader;

public class Main {

    public static void main(String[] args) {
        if (args.length > 0) {
            if (args[0].equals("--help")) printHelp();
            else new PipeLine(args);
        } else {
            System.out.println("Не хватает параметров для работы программы");
            System.out.println("--help для отображения справки");
        }
    }

    public static void printHelp() {
        String helpText = """
            ==================================================
            УТИЛИТА ФИЛЬТРАЦИИ СОДЕРЖИМОГО ФАЙЛОВ
            ==================================================
            
            НАЗНАЧЕНИЕ:
              Фильтрация содержимого входных файлов с разделением данных
              по типам (целые числа, вещественные числа, строки) и запись
              результатов в соответствующие выходные файлы.
            
            СИНТАКСИС:
              shiftjavareader [ОПЦИИ] [файл1 ... файлN]
            ОПЦИИ:
              -o ПУТЬ               Задает путь для выходных файлов
                                    По умолчанию: текущая директория
            
              -p ПРЕФИКС            Задает префикс имен выходных файлов
                                    По умолчанию: без префикса
            
              -a                    Режим добавления в существующие файлы
                                    По умолчанию: файлы перезаписываются
            
              -s                    Вывод краткой статистики
                                    (количество элементов каждого типа)
            
              -f                    Вывод полной статистики
                                    Для чисел: мин, макс, сумма, среднее
                                    Для строк: мин/макс длина строки
            
              --help                Вывод этого справочного сообщения
            
            ВЫХОДНЫЕ ФАЙЛЫ:
              <префикс>integers.txt   Целые числа
              <префикс>floats.txt     Вещественные числа
              <префикс>strings.txt    Строки
            
            ==================================================
            """;
        System.out.println(helpText);
    }

}
